// type annotation or strongly typed
console.log("Type annotation or strongly typed");
var message = "Hello World";
console.log(typeof message, message);
var marks = 93;
console.log(typeof marks, marks);
var isBeginner = true;
console.log(typeof isBeginner, isBeginner);
console.log("-----------------------------");
// type inference
// let the compiler to guess the type of the variable
console.log("Type inference");
var count = 0;
console.log(typeof count);
console.log(count);
var firstName = "John";
console.log(typeof firstName);
console.log(firstName);
